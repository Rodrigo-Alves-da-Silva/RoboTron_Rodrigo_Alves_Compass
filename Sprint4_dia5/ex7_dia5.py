#Exercício 7 dia 5 - Crie um programa que contêm uma função que receba dois parâmetros inteiros e retorna a média dos dois valores

def funcao_soma(x1, x2):
    media = (x1+x2)/2

    return media

# para executar coloque os valores os quais deseja obter a media dentro do parenteses em funcao_soma(valor1, valor2) logo abaixo:
teste = funcao_soma(5,10)
